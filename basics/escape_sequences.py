# Quotes k andar quotes or what we call as ILLEGAL CHARACTERS
# Used with print statements

"""
    \n  ->  new line
    \t  -> new tab (1 tab = 4 spaces)
    \"  -> Double quotes in output
    \'  -> Single quote in output
    \\  -> back slash in output
"""

print("Hello world \n I am Aman \n i'm a male")

# to get double quotes in output
print("My name is \"aman\"")

# get \ in output
print("25B//n")